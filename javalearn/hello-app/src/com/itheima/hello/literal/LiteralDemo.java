package com.itheima.hello.literal;

public class LiteralDemo {
    public static void main(String[] args) {
        //目标：掌握常见数据在程序中的书写格式
        //1.整数
        System.out.println(666);
        //2.小数
        System.out.println(11.1);
        //3.字符  必须用单引号，有且只能有一个字符
        System.out.println('w');
        System.out.println(' ');
//        System.out.println(''); 不能没有
        //字符串  必须用双引号，内容无所谓
        System.out.println("");
        System.out.println("sadsasd");

    }
}
